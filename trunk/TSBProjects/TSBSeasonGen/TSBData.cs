		private static string GetPosition(int index, ArrayList _positions, string[] positions)
		{
			if( _positions == null )
			{
				if( index < positions.Length )
					return positions[index];
				else
					return positions[ positions.Length -1];
			}
			else
			{
				if( index < _positions.Count )
					return _positions[index].ToString();
				else
					return _positions[ _positions.Count -1 ].ToString();
			}
		}


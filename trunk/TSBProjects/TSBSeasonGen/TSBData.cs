		public static void InitOptions()
		{
			try
			{
				ArrayList lines = GetStrings("SeasonGenOptions.txt");
				if( lines != null)
				{
					StringBuilder sb = new StringBuilder(1000);
					foreach( string line in lines)
					{
						if(line.Length > 1 )
							sb.Append(line +"\n");
					}
					string contents = sb.ToString();

					int test2 = GetIntOption("FRONT_7_MIN_SIM_PASS_RUSH", contents);
					int test1 = GetIntOption("FRONT_7_SIM_POINT_POOL", contents);
				
					if( test1 > -1 && test1 < 256 )
						FRONT_7_SIM_POINT_POOL    = test1;

					if( test2 > -1 && test2 < 29 )
						FRONT_7_MIN_SIM_PASS_RUSH = test2;

					QB_PC          = GetArray("QB_PC", contents);
					RB_MS          = GetArray("RB_MS", contents);
					WR_MS          = GetArray("WR_MS", contents);
					RETURN_MAN_MS  = GetArray("RETURN_MAN_MS", contents);
					DB_PI          = GetArray("DB_PI", contents);
					DL_LB_PI       = GetArray("DL_LB_PI", contents);
					WR_REC         = GetArray("WR_REC", contents);
					RB_REC         = GetArray("RB_REC", contents);
					TE_REC         = GetArray("TE_REC", contents);
					TE_MS          = GetArray("TE_MS", contents);
					WR_RB_TE_BC    = GetArray("WR_RB_TE_BC", contents);
				}
			}
			catch(Exception e )
			{
				MainClass.AddError(string.Format(
					"ERROR Reading options File. {0}",e.Message));
			}
		}

		/// <summary>
		/// 
		/// </summary>
		/// <param name="arrayName">The name of the array to extract from the optiins file.</param>
		/// <param name="contents">The contents of the options file (comments removed).</param>
		/// <returns>An ArrayList with the values from the options file.</returns>
		public static ArrayList GetArray(string arrayName, string contents)
		{
			int index = -1, start = -1, end = -1;

			ArrayList ret = new ArrayList();
			
			index = contents.IndexOf(arrayName);
			if( index > -1 )
				start = contents.IndexOf('{', index);
			if( start > -1 )
				end   = contents.IndexOf('}', start);

			if( index > -1 && start > -1 && end > -1 )
			{
				string str = contents.Substring( start, end - start );
				str = str.Replace("\n","").Replace("\r","");
				Regex reg = new Regex("\\s*([0-9]{1,4})\\s*");
				MatchCollection mc = reg.Matches(str);
				int tmp;
				foreach( Match m in mc )
				{
					tmp = Int32.Parse(m.Groups[1].ToString());
					ret.Add(tmp);
				}

				// if there's an unpaired int, remove it.
				if( ret.Count % 2 != 0)
				{
					ret.Remove( ret[ret.Count-1] );
					MainClass.AddError(string.Format(
                        "ERROR! Unpaired attribute in '{0}'.",
						arrayName ));
				}

				for( int i =1; i < ret.Count; i += 2 )
				{
					int k = (int) ret[i];
					if( ! IsValidTSBNumber( k ) )
					{
						MainClass.AddError(String.Format(
                           "ERROR! Invalid tecmo attribute in '{0}' attribute '{1}'.",
							arrayName, k ));
					}
				}
				if( ret.Count % 2 != 0)
				{
					MainClass.AddError(string.Format(
                      "ERROR! Unpaired value in list '{0}'.",arrayName));
				}
			}
			return ret;
		}

		public static bool IsValidTSBNumber(int num )
		{
			int[] stuff = 
		   {
				   6,   
			   13,  
			   19,
			   25, 
			   31, 
			   38, 
			   44,
			   50,
			   56,
			   63,
			   69,
			   75,
			   81,
			   88,
			   94,
			   100};
			for(int i =0; i < stuff.Length; i++)
				if(stuff[i] == num)
					return true;

			return false;

		}

		private static int GetIntOption(string option, string contents )
		{
			Regex reg = new Regex(string.Format("{0}\\s*=\\s*([0-9]+)",option));
			Match m = reg.Match(contents);
			if( m != Match.Empty )
			{
				string s = m.Groups[1].ToString();
				try
				{
					int ret = Int32.Parse(s);
					return ret;
				}
				catch
				{
					return -1;
				}
			}
			else
				return -1;
		}

		private static ArrayList GetStrings(string fileName )
		{
			ArrayList ret = null;
			if( File.Exists(fileName ) )
			{
				try
				{
					string[] lines = InputReader.GetLines(fileName);
					ret = new ArrayList(lines.Length );
					foreach( string line in lines)
					{
						if( line!= null && line.Length > 7 && !line.Trim().StartsWith("#"))
							ret.Add(line);
					}
				}
				catch(Exception e)
				{
					
					MainClass.AddError(string.Format(
                      "Error reading {0}.\n{1}",fileName,e.Message));
				}
			}
			return ret;
		}


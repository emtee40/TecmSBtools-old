		public static string GetPlayNameByPlayNumber(int number)
		{
			string num = string.Format("{0:x2}",number);
			return GetPlayNameByPlayNumber(num);
		}

		public static string GetPlayNameByPlayNumber(string number)
		{
			string ret = null;
			string searchStr = number+",";
			int numIndex   = OriginalRomPlays.IndexOf(searchStr);
			int beginLine  = 0;
			int firstComma = 0;

			if( numIndex > 0 )
			{
				char c;
				for(int i = numIndex; i > 0; i--)
				{
					c = OriginalRomPlays[i];
					if( c == '\n')
					{
						beginLine = i;
						break;
					}
				}
				int end = OriginalRomPlays.Length;
				// find first comma
				for(int i = beginLine; i < end; i++)
				{
					c = OriginalRomPlays[i];
					if( c == ',')
					{
						firstComma = i;
						break;
					}
				}
				ret = OriginalRomPlays.Substring(beginLine+1, firstComma-beginLine-1);
			}
			return ret;
		}

		public static ArrayList OffensivePlaysPointerIsIn(int ptr)
		{
			ArrayList list = new ArrayList(40);
			if( m_OriginalPlays == null)
				InitOriginalPlays();
			int outerLength = m_OriginalPlays.Length;
			int innerLength = 0;

			for(int i =0; i < m_OriginalPlays.Length; i++)
			{
				innerLength = m_OriginalPlays[i].Length;
				for(int j =0; j < innerLength; j++)
				{
					if(ptr == m_OriginalPlays[i][j])
					{
						if( !list.Contains(i) )
							list.Add(i);
						break;
					}
				}
			}
			return list;
		}
